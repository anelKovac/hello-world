public class ZapreminaValjka
{
  public static void main(String[] args)
  {
    float PI = 3.14F;
    int r = 10;
    int H = 50;
    float V = (r*r) * PI * H;
    System.out.println("Zapremina valjka cija je visina 50 \na poluprecnik osnove 10 iznosi: " + V);
  }

}
